    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        String kode_sewa = "S";
        String nomor_transaksi1 = null;
        String tahun = null;
        String kosong = null;
        Connection conn = DatabaseConnectivity.getConnection();
        try {
            Calendar cal = Calendar.getInstance();
            int year = cal.get(Calendar.YEAR);
            tahun = String.valueOf(year);
            String sql = "select no_transaksi,year(now()) as tahun from penyewaan order by no_transaksi asc";
            java.sql.Statement stat = conn.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String nomor_transaksi = hasil.getString("no_transaksi");
                nomor_transaksi1 = nomor_transaksi;
                String tahun1 = hasil.getString("tahun");
            }
            if(nomor_transaksi1==null){
                nomor_transaksi1="S0000000";
            }
            String sub_nomor = nomor_transaksi1.substring(5,8);
            int sub_nomor1 = Integer.parseInt(sub_nomor);
            sub_nomor = String.valueOf(sub_nomor1);
            int panjang = sub_nomor.length();
            switch (panjang) {
                case 1:
                    kosong = "00";
                    break;
                case 2:
                    kosong = "0";
                    break;
                case 3:
                    kosong = null;
                    break;
                default:
                    break;
            }
            sub_nomor1=sub_nomor1+1;
            sub_nomor=String.valueOf(sub_nomor1);
            text_no_transaksi.setText(kode_sewa+tahun+kosong+sub_nomor);
        } catch (SQLException | NumberFormatException e) {
        }
        text_no_transaksi.setEnabled(false);
        text_nama_penyewa.setEnabled(true);
        text_id_penyewa.setEnabled(false);
        text_no_kamar.setEnabled(true);
        text_tanggal_sewa.setEnabled(true);
        text_harga.setEnabled(false);
        text_uang_bayar.setEnabled(true);
        text_kembalian.setEnabled(false);
        tambah.setEnabled(false);
        simpan.setEnabled(true);
        cancel.setEnabled(true);
        delete.setEnabled(false);
        cetak.setEnabled(true);
    }//GEN-LAST:event_tambahActionPerformed

