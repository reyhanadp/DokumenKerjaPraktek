    private void text_cari_penyewaanKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_cari_penyewaanKeyPressed
        // TODO add your handling code here:
        String cari = text_cari_penyewaan.getText();
        Object []baris = {"No. Transaksi","Id Penyewa","Nama Penyewa","No. Kamar","Tanggal Sewa","Harga Sewa"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_cari_penyewaan.setModel(tabmode);

        //panggil method koneksi
        Connection conn = DatabaseConnectivity.getConnection();

        //untuk menampilkan di table
        try{
            String sql="Select * from penyewaan where "
            + "no_transaksi like '%"+cari+"%' "
            + "OR id_penyewa like '%"+cari+"%' "
            + "OR nama_penyewa like '%"+cari+"%' "
            + "OR no_kamar like '%"+cari+"%' "
            + "OR tanggal_sewa like '%"+cari+"%' "
            + "OR total_bayar like '%"+cari+"%' "
            + "order by no_kamar asc";
            java.sql.Statement stmt=conn.createStatement();
            java.sql.ResultSet hasil=stmt.executeQuery(sql);
            while(hasil.next()){
                String no_transaksi = hasil.getString("no_transaksi");
                String id_penyewa = hasil.getString("id_penyewa");
                String nama_penyewa = hasil.getString("nama_penyewa");
                String no_kamar = hasil.getString("no_kamar");
                String tanggal_sewa = hasil.getString("tanggal_sewa"); 
                String total_bayar = hasil.getString("total_bayar");
                String[] data = {no_transaksi, id_penyewa, nama_penyewa, no_kamar, tanggal_sewa, total_bayar};
                tabmode.addRow(data);
            }
        }
        catch(Exception ex){
        }
    }//GEN-LAST:event_text_cari_penyewaanKeyPressed

